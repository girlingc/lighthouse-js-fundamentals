// Getting user's age
const age = 0

// Using conditional statements to choose which school
if (age < 13) {
  console.log(`You are ${age}, you will go to Elementary School!`)
} else if (13 <= age && age <= 18) {
  console.log(`You are ${age}, you will go to Secondary School!`)
} else {
  console.log(`You are ${age}, you will go to Lighthouse Labs`)
}